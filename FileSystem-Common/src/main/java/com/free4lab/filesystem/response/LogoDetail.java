package com.free4lab.filesystem.response;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlRootElement;

/**
 * Created by lizhenhao on 2017/7/30.
 */
@XmlRootElement
@XmlAccessorType(XmlAccessType.FIELD)
public class LogoDetail {

    private Integer id;
    private String name;
    private String type;
    private String relativePath;
    private String absolutePath;
    private String relativePathServer;
    private String absolutePathServer;
    private String enterpriseId;
    private String date;

    public LogoDetail() {
    }

    public LogoDetail(Integer id, String name, String type, String relativePath, String absolutePath, String relativePathServer, String absolutePathServer, String enterpriseId) {
        this.id = id;
        this.name = name;
        this.type = type;
        this.relativePath = relativePath;
        this.absolutePath = absolutePath;
        this.relativePathServer = relativePathServer;
        this.absolutePathServer = absolutePathServer;
        this.enterpriseId = enterpriseId;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getRelativePath() {
        return relativePath;
    }

    public void setRelativePath(String relativePath) {
        this.relativePath = relativePath;
    }

    public String getAbsolutePath() {
        return absolutePath;
    }

    public void setAbsolutePath(String absolutePath) {
        this.absolutePath = absolutePath;
    }

    public String getRelativePathServer() {
        return relativePathServer;
    }

    public void setRelativePathServer(String relativePathServer) {
        this.relativePathServer = relativePathServer;
    }

    public String getAbsolutePathServer() {
        return absolutePathServer;
    }

    public void setAbsolutePathServer(String absolutePathServer) {
        this.absolutePathServer = absolutePathServer;
    }

    public String getEnterpriseId() {
        return enterpriseId;
    }

    public void setEnterpriseId(String enterpriseId) {
        this.enterpriseId = enterpriseId;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
